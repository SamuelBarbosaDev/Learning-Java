public class Variables {
    public static void main(String[] args){
        // Declarando a variável
        String nome;

        // Atribuindo valor
        nome = "Zé da moeda";

        // Declarando e atribuindo valor a variável
        final double PI = 3.14;

        // Usando as variáveis
        System.out.println("nome: " + nome);
        System.out.println("PI: " + PI);
    } 
}
